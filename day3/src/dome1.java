public class dome1 {
    public static void main(String[] args) {
        //整数参与运算只能得到整数
        // 在代码中，如果有小数参与计算，结果有可能不精确。

        System.out.println(1 + 1.1);
        System.out.println(10.5 - 1.01);
        System.out.println(1.5 * 1.1);
        System.out.println(10 / 3);
        System.out.println(10.0 / 3);
    }
}